LIBRARY IEEE;
USE ieee.std_logic_1164.ALL;
USE ieee.numeric_std.ALL;

ENTITY controller IS
PORT(
    RST,CLK    : IN STD_LOGIC;
    Start_i    : IN STD_LOGIC;

    a_comp     : IN STD_LOGIC;
    a_lt_0     : IN STD_LOGIC;
    A_gt       : IN STD_LOGIC;
    A_lt       : IN STD_LOGIC;
    i_comp     : IN STD_LOGIC;

    A_ld            : OUT STD_LOGIC;

    Sc_sel          : OUT STD_LOGIC;
    Sc_ld           : OUT STD_LOGIC;
    Sc_new_sel      : OUT STD_LOGIC;

    i_sel           : OUT STD_LOGIC;
    i_ld            : OUT STD_LOGIC;

    A_sel           : OUT STD_LOGIC;
    X_sel           : OUT STD_LOGIC;
    Y_sel           : OUT STD_LOGIC;

    X_ld            : OUT STD_LOGIC;
    Y_ld            : OUT STD_LOGIC;

    A_shift_sel     : OUT STD_LOGIC;

    Sqrt_ld         : OUT STD_LOGIC;
    Sqrt_sel        : OUT STD_LOGIC;

    Done_o          : OUT STD_LOGIC;
    err             : OUT STD_LOGIC
);
END controller;

ARCHITECTURE bev OF controller IS

TYPE State_type IS (
    S0,
    S1,
    S2,
    S3,
    S4,
    S5,
    S6,
    S7,
    S8,
    S9,
    S10,
    S11,
    S12,
    S13,
    S14,
    S15,
    S16
);

SIGNAL State : State_type;

BEGIN

-------------------------------------------------------------------------------
-- FSM
-------------------------------------------------------------------------------
PROCESS(RST, CLK)
BEGIN

    IF RST = '1' THEN
        State <= S0;

    ELSIF rising_edge(CLK) THEN

        CASE State IS

            -------------------------------------------------------------------
            WHEN S0 =>
                State <= S1;

            -------------------------------------------------------------------
            WHEN S1 =>

                IF Start_i = '1' THEN

                    IF a_comp = '1' THEN
                        State <= S2;

                    ELSIF a_lt_0 = '1' THEN
                        State <= S3;

                    ELSE
                        State <= S4;
                    END IF;

                ELSE
                    State <= S1;
                END IF;

            -------------------------------------------------------------------
            -- input = 0
            WHEN S2 =>
                State <= S16;

            -------------------------------------------------------------------
            -- input < 0
            WHEN S3 =>
                State <= S16;

            -------------------------------------------------------------------
            -- init scale
            WHEN S4 =>
                State <= S5;

            -------------------------------------------------------------------
            -- load A
            WHEN S5 =>
                State <= S6;

            -------------------------------------------------------------------
            -- normalize
            WHEN S6 =>

                IF A_lt = '1' THEN
                    State <= S7;

                ELSIF A_gt = '1' THEN
                    State <= S8;

                ELSE
                    State <= S9;
                END IF;

            -------------------------------------------------------------------
            -- shift left A
            WHEN S7 =>
                State <= S6;

            -------------------------------------------------------------------
            -- shift right A
            WHEN S8 =>
                State <= S6;

            -------------------------------------------------------------------
            -- init X,Y
            WHEN S9 =>
                State <= S10;

            -------------------------------------------------------------------
            -- init i
            WHEN S10 =>
                State <= S11;

            -------------------------------------------------------------------
            -- iteration loop
            WHEN S11 =>

                IF i_comp = '1' THEN
                    State <= S15;
                ELSE
                    State <= S12;
                END IF;

            -------------------------------------------------------------------
            WHEN S12 =>
                    State <= S13;
            -------------------------------------------------------------------
            WHEN S13 =>
                State <= S14;
            -------------------------------------------------------------------
            WHEN S14 =>
                State <= S11;

            -------------------------------------------------------------------
            WHEN S15 =>
             State <= S16;
            -------------------------------------------------------------------
            WHEN S16 =>
                State <= S0;
            -------------------------------------------------------------------
            WHEN OTHERS =>
                State <= S0;

        END CASE;

    END IF;

END PROCESS;

-------------------------------------------------------------------------------
-- OUTPUT LOGIC
-------------------------------------------------------------------------------
PROCESS(State)
BEGIN

    ---------------------------------------------------------------------------
    -- DEFAULT
    ---------------------------------------------------------------------------

    A_ld <= '0';

    Sc_sel <= '0';
    Sc_ld <= '0';
    Sc_new_sel <= '0';

    i_sel <= '0';
    i_ld <= '0';

    A_sel <= '0';
    X_sel <= '0';
    Y_sel <= '0';

    X_ld <= '0';
    Y_ld <= '0';


    A_shift_sel <= '0';

    Sqrt_ld <= '0';
    Sqrt_sel <= '0';

    Done_o <= '0';
    err <= '0';

    ---------------------------------------------------------------------------
    CASE State IS
	
        -----------------------------------------------------------------------
        WHEN S2 =>
            -- sqrt(0)=0
            Sqrt_sel <= '0';
            Sqrt_ld <= '1';

        -----------------------------------------------------------------------
        WHEN S3 =>
            err <= '1';

        -----------------------------------------------------------------------
        -- init scale = 0
        WHEN S4 =>
            Sc_sel <= '0';
            Sc_ld <= '1';

        -----------------------------------------------------------------------
        -- load A
        WHEN S5 =>
            A_sel <= '0';
            A_ld <= '1';

        -----------------------------------------------------------------------
        -- A = A << 2
        -- scale--
        WHEN S7 =>
            A_shift_sel <= '0';
            Sc_sel <= '1';
            Sc_ld <= '1';
            Sc_new_sel <= '0';
	    A_sel <= '1';
            A_ld <= '1';
        -----------------------------------------------------------------------
        -- A = A >> 2
        -- scale++
        WHEN S8 =>
            A_shift_sel <= '1';
            Sc_sel <= '1';
            Sc_ld <= '1';
            Sc_new_sel <= '1';
	    A_sel <= '1';
            A_ld <= '1';
        -----------------------------------------------------------------------
        -- X=A+0.25
        -- Y=A-0.25
        WHEN S9 =>

            X_sel <= '0';
            X_ld <= '1';

            Y_sel <= '0';
            Y_ld <= '1';

        -----------------------------------------------------------------------
        -- i=1
        WHEN S10 =>

            i_sel <= '0';
            i_ld <= '1';

        WHEN S13 =>

            X_sel <= '1';
            X_ld <= '1';

            Y_sel <= '1';
            Y_ld <= '1';

        -----------------------------------------------------------------------
        -- i++
        WHEN S14 =>

            i_sel <= '1';
            i_ld <= '1';

        -----------------------------------------------------------------------
	--update sqrt
        WHEN S15 =>
            Sqrt_sel <= '1';
            Sqrt_ld <= '1';

        -----------------------------------------------------------------------
        WHEN S16 =>
            Done_o <= '1';

        -----------------------------------------------------------------------
        WHEN OTHERS =>
            NULL;

    END CASE;

END PROCESS;

END bev;
